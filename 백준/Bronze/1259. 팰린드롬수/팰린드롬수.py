while True:
    num = input().strip()  # 사용자 입력 받기

    if num == "0":  # 입력값이 "0"이면 종료
        break
    
    reversed_num = num[::-1]  # 입력값을 뒤집음
    
    if num == reversed_num:  # 원래 숫자와 뒤집은 숫자가 같으면
        print("yes")  # 팰린드롬이므로 "yes" 출력
    else:
        print("no")  # 팰린드롬이 아니므로 "no" 출력
        
        
'''
# 팰린드롬 확인 방법
- 숫자를 문자열로 변환한 뒤,뒤집은 문자열과 비교 --> 같으면 yes, 다르면 no 출력.
- 여러 개의 숫자가 주어질 때, 마지막 0이 입력되면 종료--> 0이 입력되면 반복문 종료.
- 최대 5자리(99999)이므로 단순 문자열 비교로 풀이가능

'''        
